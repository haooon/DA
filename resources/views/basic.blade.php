                <h2 class="ui big center aligned header smallheader">意义</h2>
                <p>
                    当代大学生思想主流积极、健康、向上，
                    但他们同时具有思想复杂多变、价值取向多元化、生活方式个性化、利益诉求多样化等特点。
                    当代大学生舆情具有诱因多源化、传播方式信息化、内容复杂化、影响严重化等特点。
                    高校应坚持以人为本、统一协调、上下联动、分级预警、讲求策略原则，
                    建立大学生舆情工作组织体系；构建大学生<strong>舆情汇集</strong>、
                    <strong>舆情分析研判</strong>、<strong>舆情预防监控</strong>、
                    <strong>舆情干预处置</strong>、<strong>舆情跟踪反馈</strong>等工作机制。
                </p>
                <h2 class="ui big center aligned header smallheader">来源</h2>
                <p>
                    为了选择合理的平台进行舆情数据的
                    <strong>采集</strong>、
                    <strong>跟踪</strong>、
                    <strong>分析</strong>
                    。我们设计了面向大学生的问卷调查，
                    在项目开始初期对50个学生进行了问卷调查。
                    问卷涉及的范围有大学生日网络活动时长，网络活动范围及类型，
                    对各类信息的关注程度，活跃度以及和好友间活跃的程度。
                </p>
                <p>
                    根据调查问卷的结果得出大学生平均日常参与网络活动的时间，
                    以及大学生日常网络活动所关注的内容，和最多使用的平台。
                </p>
                <div id="user_time" class="ui container" style="width: 600px;height:400px;"></div>
                <h3 class="ui big center aligned header smallheader">[大学生日常网上活动时间结果]</h3>
                <p>
                    从结果可以得出大学生日常网上活动时长平均值为4小时左右，假设预估一天除去睡眠时间大概有16个小时
                    （早8点 - 晚12点）使用网络时间占了日常活动时间的四分之一左右，学生有大量的时间可以受到网络思想的影响。
